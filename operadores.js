// Incremento / decremento
var x =1
console.log(x=x+1) // 2
console.log(x++)   // 2  com x = 3
console.log(x)
console.log(x=x-1) // 2
console.log(x--)   // 2 com x = 1
console.log(x)
console.log(x+=4)
console.log(x/=5)

// Matemáticos
